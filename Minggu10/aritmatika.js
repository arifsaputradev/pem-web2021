var nilaiA = 10;
var nilaiB = 11;
var hasil = 0;

// Penjumlahan
hasil = nilaiA + nilaiB;
document.write(`${nilaiA} + ${nilaiB} = ${hasil} <br>`);

// Pengurangan
hasil = nilaiA - nilaiB;
document.write(`${nilaiA} - ${nilaiB} = ${hasil} <br>`);

// Perkalian
hasil = nilaiA * nilaiB;
document.write(`${nilaiA} * ${nilaiB} = ${hasil} <br>`);

// Pemangkatan
hasil = nilaiA ** nilaiB;
document.write(`${nilaiA} ** ${nilaiB} = ${hasil} <br>`);

// Pembagian
hasil = nilaiA / nilaiB;
document.write(`${nilaiA} / ${nilaiB} = ${hasil} <br>`);

// Modulus
hasil = nilaiA % nilaiB;
document.write(`${nilaiA} % ${nilaiB} = ${hasil} <br>`);
